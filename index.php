        <h2 class="text-secondary">Sobre a Rede de diversificação de áreas cultivadas com tabaco</h2>
        <p>
            O Brasil é o segundo maior produtor de fumo do mundo e está entre os maiores exportadores, sendo que a maior parte da produção está destinada ao mercado externo.
        </p>
        <p>
            A fumicultura no Brasil se dá em pequenas propriedades de famílias de agricultores, principalmente em municípios dos estados do Sul do país. Atualmente, são mais de 150 mil famílias regidas por contratos de integração que dependem economicamente das indústrias do tabaco.
        </p>
        <p>
            Embora a produção de tabaco seja enaltecida como uma atividade que gera riqueza, desenvolvimento e emprego, estudos mostram que a renda média mensal das famílias por trabalhador é de um terço do salário mínimo nacional. A análise do Índice de Desenvolvimento Humano (IDH – expectativa de vida, taxa de alfabetização, taxa de frequência escolar e renda per capita) mostra que as principais áreas produtoras de fumo na região Sul apresentam média abaixo do índice estadual, e a taxa de frequência escolar e renda nos municípios onde predominam a fumicultura são inferiores a dos municípios onde não se produz tabaco. A produção também afeta a saúde dos trabalhadores e a região fumicultora está entre as que têm alto índice de suicídios.
        </p>
        <p>
            Em 2005, o governo federal criou o Programa Nacional de Diversificação em Áreas Cultivadas com Tabaco para melhorar a qualidade de vida dos produtores e salvaguardá-los dos efeitos de diminuição do consumo de tabaco esperados pela implementação da Convenção-Quadro para Controle do Tabaco da Organização Mundial da Saúde. Entretanto, o programa corre riscos diante da falta de investimentos.
        </p>
        <p>
            Aqui você pode encontrar informações relacionadas ao tema de diversificação, os principais desafios enfrentados no setor, experiências positivas dos projetos e as atividades desenvolvidas pelas entidades que atenderam à chamada pública de diversificação de 2018.
        </p>
        
